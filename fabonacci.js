//   Fibonacci  সংখ্যা বা ধারা যেভাবে বের করতে হবে । আমাদের যেকোনো  n  তম পদের মান বের করতে হবে । 

// n তম পদ = [n-1] + [n-2]  তম পদ 

const fibo = [0, 1];
for(let i = 2; i <= 10; i++){
    fibo[i] = fibo[i-1] + fibo[i-2]; // এখানে  i এর মান চেক করে , পূর্বের সংখ্যার সাথে পরের সাথে  যোগ করে । ধারা পরিবর্তন করবে । 
}
console.log(fibo);