// Math.random ()  - এখানে সংখ্যাগুলো এলোমেলোভাবে দিবে । 
// Math.ceil ()   - সিলিং , অর্থ্যাৎ দশমিকের পরে যদি কোন সংখ্যা থাকে তাহলে সেই সংখ্যাকে উপরের দিকে নিয়ে যাবে ।
// Math.floor ()  - এখানে কোন সংখ্যার পরে দশমিক সংখ্যা থাকলে, সেই সংখ্যাকে নিচের দিকে নিয়ে আসবে । 
// Math.round ()  - রাউন্ড সংখ্যা দ্বারা ঐ সংখ্যার সমান হবে মাঝে মাঝে । 