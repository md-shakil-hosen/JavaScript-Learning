// ------------------Adding and Deleting Elements--------------------
//  কোন এলিমেন্ট যুক্ত করার জন্য ঃ 
// ১. document.createElement(element) - কোন নতুন এলিমেন্ট যুক্ত করার জন্য  ব্যবহার করা হয় ।
// ২. document.removeChild(element) -  কোন এলিমেন্ট ডিলিট করার জন্য ব্যবহার করা হয় ।
// ৩. document.appendChild(element) - এইচটিএমল এলিমেন্টের মধ্যে কোন নতুন এলিমেন্ট যুক্ত করতে এটি ব্যবহার করা হয় । 
// ৪. -document.replaceChild(new, old) - এইটএমএল এর মধ্যে কোন একটি এলিমেন্টের জায়গায় অন্য কোন নতুন এলিমেন্ট যুক্ত করার জন্য এটি ব্যবহার করা হয় । 


